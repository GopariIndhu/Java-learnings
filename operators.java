package FirstProgram;

public class operators1 {

	public static void main(String[] args) {
		int number1 = 33;
		int number2 = 22;
		//is equal to
		System.out.println(number1 == number2);
		//is not equal to
		System.out.println(number1 != number2);
		//greater than
		System.out.println(number1 > number2);
		//less than
		System.out.println(number1 < number2);
		//greater than or equal to
		System.out.println(number1 >= number2);
		//less than or equal to
		System.out.println(number1 <= number2);
	}

}
